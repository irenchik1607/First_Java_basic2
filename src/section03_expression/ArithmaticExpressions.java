package section03_expression;

public class ArithmaticExpressions {
    public static void main(String[] args) {
        {
            var a = 5;
            var b = 3;
            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a * b);
            System.out.println(a / b);
            System.out.println(a % b); // остаток от деления
        }
        {
            var a = 5.;
            var b = 3.;
            System.out.println(a + b);
            System.out.println(a - b);
            System.out.println(a * b);
            System.out.println(a / b);
            System.out.println(a % b); // остаток от деления
        }

    }
}
